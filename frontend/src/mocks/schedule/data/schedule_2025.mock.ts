const toStrapiImage = (
  path: string | undefined
): { id: number; documentId: string; url: string } | undefined => {
  if (!path) return undefined;

  const id =
    path.split("").reduce((acc, char) => acc + char.charCodeAt(0), 0) % 1000000;
  const documentId = path.replace(/\//g, "_").replace(/\./g, "_");
  return { id, documentId, url: path };
};

